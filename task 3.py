def count_letters(text):
    letter_count = {}  # Словарь для хранения количества букв
    total_letters = 0   # Переменная для хранения общего количества букв

    for char in text:
        if char.isalpha():  # Проверяем, является ли символ буквой
            char = char.lower()  # Приводим букву к нижнему регистру
            total_letters += 1  # Увеличиваем общее количество букв

            if char in letter_count:
                letter_count[char] += 1  # Увеличиваем счетчик буквы
            else:
                letter_count[char] = 1  # Добавляем букву в словарь

    # Печать буквы и её частоты в столбик
    print("Буква | Частота")
    print("----------------")
    for letter, count in letter_count.items():
        frequency = count / total_letters  # Вычисляем частоту
        print(f"{letter}     | {frequency:.4f}")  # Выводим букву и её частоту


